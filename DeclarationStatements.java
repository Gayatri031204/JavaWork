package allprograms;

public class DeclarationStatements {
public static void main(String[] args) {
	int age =19;      // declare & integer variable & assign value 
	double price=99.99;   // declare a double variable & assign a value
	char grade='A';       // declare a character variable 
	String str="John";   //  decalre a string variable
	System.out.println("Age:  " + age);
	System.out.println("Price: " + price);
	System.out.println("Grade: " + grade);
	System.out.println("str: " + str);
}
}
