package allprograms;

public class Byteprogram {
public static void main(String[] args) {
	// valid positive integer range
	byte a = 127;
	byte b= 0;
	System.out.println("byte range: " + a);
	System.out.println("byte range: " + b);
	// valid nagative integer range
	byte f = -128;
	byte g = -0;
	System.out.println("byte range: " + f);
	System.out.println("byte range: "+ g );
	 
}
}
